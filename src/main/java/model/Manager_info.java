package model;

public class Manager_info {

	private String manager_id;
	private String manager_number;
	private String manager_state;
	
	public String getManager_id() {
		return manager_id;
	}
	public void setManager_id(String manager_id) {
		this.manager_id = manager_id;
	}
	public String getManager_number() {
		return manager_number;
	}
	public void setManager_number(String manager_number) {
		this.manager_number = manager_number;
	}
	public String getManager_state() {
		return manager_state;
	}
	public void setManager_state(String manager_state) {
		this.manager_state = manager_state;
	}
	
	
	
}
