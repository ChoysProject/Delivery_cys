package model;

public class Menu_info {

	
	private String menu_id;
	private String menu_name;
	private Integer menu_price;
	private Integer menu_some;
	private Integer menu_calorie;
	private String menu_picture;
	public String getMenu_id() {
		return menu_id;
	}
	public void setMenu_id(String menu_id) {
		this.menu_id = menu_id;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public Integer getMenu_price() {
		return menu_price;
	}
	public void setMenu_price(Integer menu_price) {
		this.menu_price = menu_price;
	}
	public Integer getMenu_some() {
		return menu_some;
	}
	public void setMenu_some(Integer menu_some) {
		this.menu_some = menu_some;
	}
	public Integer getMenu_calorie() {
		return menu_calorie;
	}
	public void setMenu_calorie(Integer menu_calorie) {
		this.menu_calorie = menu_calorie;
	}
	public String getMenu_picture() {
		return menu_picture;
	}
	public void setMenu_picture(String menu_picture) {
		this.menu_picture = menu_picture;
	}
	
	

	
	
	
}
