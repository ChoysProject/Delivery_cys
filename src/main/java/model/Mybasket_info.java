package model;

public class Mybasket_info {

	private Integer seqno;
	private String id;
	private String code;
	private Integer num;
	private String mybasket_name;
	private Integer mybasket_calorie;
	private Integer mybasket_price;
	private String mybasket_picture;
	
	
	public Integer getSeqno() {
		return seqno;
	}
	public void setSeqno(Integer seqno) {
		this.seqno = seqno;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public String getMybasket_name() {
		return mybasket_name;
	}
	public void setMybasket_name(String mybasket_name) {
		this.mybasket_name = mybasket_name;
	}
	public Integer getMybasket_calorie() {
		return mybasket_calorie;
	}
	public void setMybasket_calorie(Integer mybasket_calorie) {
		this.mybasket_calorie = mybasket_calorie;
	}
	public Integer getMybasket_price() {
		return mybasket_price;
	}
	public void setMybasket_price(Integer mybasket_price) {
		this.mybasket_price = mybasket_price;
	}
	public String getMybasket_picture() {
		return mybasket_picture;
	}
	public void setMybasket_picture(String mybasket_picture) {
		this.mybasket_picture = mybasket_picture;
	}
	
	
	
	
	
}
